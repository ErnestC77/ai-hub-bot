import type { ModelOut } from "@/api/client";

/**
 * Выбирает модель, которая должна быть активной при открытии чата или генерации.
 *
 * Вызывается из трёх мест: ModelPicker (чат), /generate-image, /generate-video.
 * Список `models` уже отфильтрован по категории бэкендом и отсортирован по sort_order,
 * поэтому models[0] -- это «первая по порядку», а не случайная.
 *
 * @param models       модели своей категории из api.models(...); может быть пустым
 * @param urlCode      код из ?model= -- пользователь тапнул конкретную карточку на Home.
 *                     Может не найтись в списке: админка могла скрыть модель, либо код
 *                     из чужой категории (видео-код прилетел в чат)
 * @param defaultCode  me.default_model_code -- постоянное предпочтение пользователя.
 *                     Тоже может отсутствовать в списке
 * @returns выбранная модель, либо null если выбирать не из чего
 */
export function resolveModel(
  models: ModelOut[],
  urlCode: string | null,
  defaultCode: string | null | undefined,
): ModelOut | null {
  // Приоритет: сиюминутное намерение важнее постоянного предпочтения.
  // Тап по карточке «Kling» на Home должен открыть Kling -- иначе карточки моделей
  // бессмысленны, они все вели бы в одно и то же. Дефолт юзера работает при заходе
  // через action-карточку или таббар, где модель не названа.
  const byCode = (code: string | null | undefined) =>
    code ? models.find((m) => m.code === code) : undefined;

  // Ненайденный код -- тихий откат на следующий приоритет: модель могли скрыть
  // в админке или код прилетел из чужой категории (видео-код в чат). Ошибку не
  // показываем: юзер не выбирал недоступную модель осознанно, он тапнул карточку,
  // которую ему нарисовали.
  return byCode(urlCode) ?? byCode(defaultCode) ?? models[0] ?? null;
}
